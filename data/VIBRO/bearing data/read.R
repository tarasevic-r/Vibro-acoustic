# For the names of the data files, for example 'B007_0', 
# the first letter represents fault position, next three numbers represent
# fault diameters (0.007, 0.014, 0.021 inches) and the last number represents
# bearing loads(0,1,2,3). There are three kinds of fault positions here, 
# B-bearing rolling element, IR-inner raceway, OR-outer raceway. 
# Data we used are all from fan end which is marked as 'FE' in the data files.
